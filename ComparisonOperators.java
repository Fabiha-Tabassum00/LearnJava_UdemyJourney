public class ComparisonOperators {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;
        System.out.println(x == y);
        System.out.println(x != y);

        int a = 10;
        int b = 5;
        System.out.println(a > b);
        System.out.println(a < b);

        int c = 10;
        int d = 10;
        System.out.println(c >= d);
        System.out.println(c <= d);
    }
}
