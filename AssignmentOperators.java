public class AssignmentOperators {
    public static void main(String[] args) {
        int x = 5; //normal assignment

        int y = 8;
        y += 3;   // x = x+3
        System.out.println(y);

        y -= 3;
        System.out.println(y);

        y *= 3;
        System.out.println(y);

        y /= 3;
        System.out.println(y);

        String name = "Kon";
        name += " Doe";
        System.out.println(name);

        double num = 3.14;
        num *= 2;
        System.out.println(num);
    }
}
